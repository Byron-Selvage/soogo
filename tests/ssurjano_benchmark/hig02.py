import numpy as np
def hig02(s):
    ##########################################################################
    # HIGHDON (2002) FUNCTION
    #####################################################################
    
    term1 = np.sin(2*np.pi*s/10)
    term2 = 0.2 * np.sin(2*np.pi*s/2.5)
    
    y = term1 + term2
    
    return(y)